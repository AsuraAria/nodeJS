const Task = require("./Task.js");

////////////////////////////////////////////////////////
//	CLASS TaskService
////////////////////////////////////////////////////////

class TaskService{
	constructor(data){  
		this.array = new Array();
	}

	addTask(data){
		this.array.push(new Task({title:data.title}));
	}

	getTask(){
		return this.array;
	}

}

module.exports = TaskService;
